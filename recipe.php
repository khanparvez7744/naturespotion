      <div class="row my-5 shadow-lg recipeRow px-sm-0 px-3" id="breadRecipe">
        <div class="col-sm-12 p-0">
          <img src="images/honey-spicy-bread.jpg" alt="" class="img-fluid">
        </div>
        <div class="col-sm-5 p-5 bg-lightWar">
          <h3>INGREDIENTS</h3>
          <hr>
          <ul class="fs-22">
            <li>2/3 cup packed brown sugar
            </li>
            <li>1/3 cup 2% milk
            </li>
            <li>2 cups all-purpose sugar
            </li>
            <li>1-1/2 teaspoons baking powder
            </li>
            <li>½ tsp ground cinnamon
            </li>
            <li>½ tsp ground nutmeg
            </li>
            <li>1/8 tbs ground cloves
            </li>
            <li>2 large eggs
            </li>
            <li>½ cup honey
            </li>
            <li>2 tbs % Milk
            </li>
          </ul>
        </div>
        <div class="col-sm-7 p-5 bg-lightWar">
          <h3>DIRECTIONS</h3>
          <hr>
          <ul class="fs-22">
            <li>Preheat oven to 325°. In a small saucepan, combine brown sugar and milk. Cook and stir over low heat
              until sugar is dissolved. Remove from heat.
            </li>
            <li>In a large bowl, whisk flour, baking powder, cinnamon, nutmeg and cloves. In another bowl, whisk eggs,
              honey, oil and brown sugar mixture until blended. Add to flour mixture; stir just until moistened.
            </li>
            <li>Transfer to a greased 8x4-in. loaf pan. Bake 50-60 minutes or until a toothpick inserted in center comes
              out clean (cover top loosely with foil if needed to prevent overbrowning).
            </li>
            <li>Cool in pan 10 minutes before removing to a wire rack to cool completely. In a small bowl, stir glaze
              ingredients until smooth; drizzle over bread.
            </li>
          </ul>
          <p class="fs-22">Freeze option: Securely wrap and freeze cooled loaf in plastic wrap and foil. To use, thaw at
            room temperature. Glaze as directed.
          </p>

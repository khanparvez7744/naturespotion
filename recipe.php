        </div>
        <div class="col-sm-5 p-5 bg-lightWar">
          <h3>INGREDIENTS</h3>
          <hr>
          <ul class="fs-22">
            <li>¾ cup canola oil
            </li>
            <li>1 cup powdered jaggery
            </li>
            <li>¼ cup honey
            </li>
            <li>1 large egg, room temperature
            </li>
            <li>2 cups all-purpose flour
            </li>
            <li>1 tsp baking soda
            </li>
            <li>1 tsp ground cinnamon
            </li>
            <li>¼ tsp salt
            </li>
            <li>1 cup chopped and toasted walnuts </li>
          </ul>
        </div>
        <div class="col-sm-7 p-5 bg-lightWar">
          <h3>DIRECTIONS</h3>
          <hr>
          <ul class="fs-22">
            <li>Preheat oven to 350°. In a large bowl, beat sugar, oil, honey and egg until well blended. In another
              bowl, whisk flour, baking soda, cinnamon and salt; gradually beat into sugar mixture. Stir in 1 cup
              walnuts.
            </li>
            <li>Spread into a greased 15x10x1-in. baking pan. Bake until golden brown (edges will puff up), 10-12
              minutes. Cool completely on a wire rack.
            </li>
            <li>For glaze, in a small bowl, mix confectioners' sugar, mayonnaise, vanilla and enough water to reach
              desired consistency; spread over top. If desired, sprinkle with additional walnuts. Let stand until set.
              Cut into bars. Refrigerate leftovers.
            </li>
          </ul>
          <p class="fs-22">Note- To toast nuts, bake in a shallow pan in a 350° oven for 5-10 minutes or cook in a
            skillet over low heat until lightly browned, stirring occasionally.
          </p>

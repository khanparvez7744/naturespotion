          <h3>INGREDIENTS</h3>
          <hr>
          <ul class="fs-22">
            <li>2 cups all-purpose flour
            </li>
            <li>½ cup brown sugar
            </li>
            <li>3 tsp baking powder
            </li>
            <li>3tsp salt
            </li>
            <li>1 large egg, room temperature
            </li>
            <li>1 cup 2% milk
            </li>
            <li>¼ cup melted butter
            </li>
            <li>¼ cup honey
            </li>

          </ul>
        </div>
        <div class="col-sm-7 p-5 bg-lightWar">
          <h3>DIRECTIONS</h3>
          <hr>
          <ul class="fs-22">
            <li>Preheat oven to 400°. In a large bowl, combine flour, sugar, baking powder and salt. In a small bowl,
              combine egg, milk, butter and honey. Stir into dry ingredients just until moistened.
            </li>
            <li>Fill greased or paper-lined muffin cups three-fourths full. Bake until a toothpick inserted in center
              comes out clean, 15-18 minutes. Cool 5 minutes before removing from pan to a wire rack. Serve warm.
            </li>
            <li>Freeze option: Freeze cooled muffins in freezer containers. To use, thaw at room temperature or, if
              desired, microwave each muffin on high until heated through, 20-30 seconds.
            </li>
          </ul>


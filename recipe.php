          <hr>
          <ul class="fs-22">
            <li>Preheat oven to 350°. In a bowl, mix shortening, peanut butter and honey. Add eggs; mix well. Combine
              flour, sugar, baking soda, baking powder and salt; add to peanut butter mixture and mix well.
            </li>
            <li>Roll into 1- to 1-1/2-in. balls and place on ungreased baking sheets. Flatten with a fork dipped in
              flour. Bake until set, 8-10 minutes. Remove to wire racks to cool. </li>
